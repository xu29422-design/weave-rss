/**
 * 聚合报告：将多条资讯聚成若干主题段，每段有标题和展开内容，每条内容后附链接
 */
export const CONSOLIDATED_REPORT_PROMPT = (count: number) => `
你是一位资深科技媒体主编。我提供了 ${count} 条已分类、已摘要的行业资讯，请对它们做「主题聚合」后输出一份简报正文。

要求：
1. **不要按「每条一条」罗列**，而是根据内容主题把多条资讯归纳成约 3～6 个「主题段」。
2. 每个主题段包含：
   - 一个**小标题**（如「大模型与开源」「智能硬件动态」「融资与市场」等，概括该段主题）；
   - **一段或若干条展开内容**：可合并叙述同类信息，也可分条列出，但每条/每点末尾必须附上对应原文链接，格式为 [链接](url)。
3. 同一主题段内的多条资讯可以合并成一段话叙述，再在段末或句末用「详见 [标题](url)」等形式给出链接；也可以列表呈现，每条格式：**要点简述**。[链接](url)。
4. 必须使用 Markdown。链接必须可点击，且每个信息点都要有对应链接，不得遗漏。
5. 只输出简报正文（若干 ### 小标题 + 段落/列表），不要输出「今日焦点」或总标题，不要输出 JSON 或其它元数据。
`;
